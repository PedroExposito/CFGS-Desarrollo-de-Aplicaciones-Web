    private void jButtonReservaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonReservaActionPerformed
        // TODO add your handling code here:
        String Caracteres = jTextFieldNombre.getText();
        String CaracteresApellido1 = Apellido1.getText();
        String tlf = Telefono.getText();            
        String date = jDateChooserFecha.getDateFormatString();
        String personas = jTextFieldPersonas.getText();
        if((Caracteres.length() == 0 || CaracteresApellido1.length() == 0
                || tlf.length() == 0 || date.length() == 0 || personas.length() == 0)){
            JOptionPane.showMessageDialog(null, "Los campos con asterisco (*) son obligatorios");
        }
        else{
            JOptionPane.showMessageDialog(null, "Reserva realizada con éxito");

        }
    }//GEN-LAST:event_jButtonReservaActionPerformed


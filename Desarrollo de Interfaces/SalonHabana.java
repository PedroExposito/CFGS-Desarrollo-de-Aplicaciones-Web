    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        // TODO add your handling code here:
        Object resultado;
        resultado = jComboBox1.getSelectedItem();
        if( resultado == "Congreso"){
            jLabelHabitacion.setEnabled(true);
            jCheckBoxHabitacion.setEnabled(true);
            jLabelPersonas.setEnabled(true);
            jTextFieldPersonas.setEnabled(true);
        }
        else{
            jLabelHabitacion.setEnabled(false);
            jCheckBoxHabitacion.setEnabled(false);
            jLabelPersonas.setEnabled(false);
            jTextFieldPersonas.setEnabled(false);
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

    private void TelefonoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TelefonoKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        String tlf = Telefono.getText();            
        if(c<'0' || c>'9' || tlf.length() >= 9){
            evt.consume();
        }
    }//GEN-LAST:event_TelefonoKeyTyped

